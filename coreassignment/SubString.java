package com.revature.coreassignment;

public class SubString{

public static void main (String [] args) {}
public static String String (String yeah, int dex) {
	String dollars = " ";
	int d = 0;
	while(d<=dex-1) {
		dollars = dollars + yeah.charAt(d);
		d++;
	}
	return yeah;
	System.out.println(yeah);
}
}
