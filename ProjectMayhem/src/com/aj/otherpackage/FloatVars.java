package com.aj.otherpackage;

public class FloatVars {
	private float f1 = 123.23f;
	private float f2 = 234.23f;
	
	public void floatVars() {
		
	}
	
	public void floatVars(float f1, float f2) {
		this.f1 = f1;
		this.f2 = f2;
	}
	
	public float getF1() {
		return f1;
	}
	public void setF1(float f1) {
		this.f1 = f1;
	}
	public float getF2() {
		return f2;
	}
	public void setF2(float f2) {
		this.f2 = f2;
	}
	
}
